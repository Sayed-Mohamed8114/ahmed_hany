  /* ══════════════════════════════════════════
     i18n ENGINE — maps data-i18n keys to DOM
  ══════════════════════════════════════════ */
  // Extended map: key → { selector, property, value_key }
  // We'll use a simple approach: data-i18n on elements sets innerHTML
  // For placeholders we use data-i18n-ph, for labels data-i18n-label etc.

  let currentLang = 'en';

  function applyTranslations(lang) {
    const t = translations[lang];
    const html = document.documentElement;

    // Fade out
    document.body.classList.add('lang-switching');

    setTimeout(() => {
      // Set direction & lang
      html.setAttribute('lang', lang);
      html.setAttribute('dir', lang === 'ar' ? 'rtl' : 'ltr');

      // Apply all data-i18n elements
      document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (t[key] !== undefined) el.innerHTML = t[key];
      });

      // Placeholders
      document.querySelectorAll('[data-i18n-ph]').forEach(el => {
        const key = el.getAttribute('data-i18n-ph');
        if (t[key] !== undefined) el.setAttribute('placeholder', t[key]);
      });

      // Labels (text content, not innerHTML)
      document.querySelectorAll('[data-i18n-label]').forEach(el => {
        const key = el.getAttribute('data-i18n-label');
        if (t[key] !== undefined) el.textContent = t[key];
      });

      // Lang toggle labels
      document.querySelectorAll('.lang-label, .lang-label-mobile').forEach(el => {
        el.textContent = t.lang_switch;
      });

      // Document title
      document.title = lang === 'ar' ? 'أحمد هاني | مستشار قانوني' : 'Ahmed Hany | Elite Legal Counsel';

      // Loader text
      document.querySelector('.loader-text').textContent = lang === 'ar' ? 'أحمد هاني' : 'AHMED HANY';

      // Fade back in
      document.body.classList.remove('lang-switching');
    }, 200);
  }

  function toggleLanguage() {
    currentLang = currentLang === 'en' ? 'ar' : 'en';
    applyTranslations(currentLang);
  }

